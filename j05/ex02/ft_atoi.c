int		ft_atoi(char *str)
{
	int i;
	int b;
	int symbol;

	b = 0;
	i = 0;
	symbol = 1;
	while (str[i] == ' ' || str[i] == '\n'
			|| str[i] == '\t' || str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			symbol = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		b = b * 10 + str[i] - 48;
		i++;
	}
	return (b * symbol);
}
