char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (to_find[j] == '\0')
		return (str);
	while (1)
	{
		while (str[i] != '\0' && str[i++] == to_find[j++])
			;
		if (to_find[j] == '\0')
			return (&str[i - j]);
		if (str[i] == '\0')
			return (0);
	}
}
