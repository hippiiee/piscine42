int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] <= 47 || (str[i] >= 58 && str[i] <= 64) || str[i] >= 123)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
